public class RequestFloatMultiply {

	float multiplyFloatVar;
	int multiplyIntVar;
	float resultVal;

	public float getMultiplyFloatVar() {
		return multiplyFloatVar;
	}

	public void setMultiplyFloatVar(float multiplyFloatVar) {
		this.multiplyFloatVar = multiplyFloatVar;
	}

	public int getMultiplyIntVar() {
		return multiplyIntVar;
	}

	public void setMultiplyIntVar(int multiplyIntVar) {
		this.multiplyIntVar = multiplyIntVar;
	}

	public float getResultVal() {
		return resultVal;
	}

	public void setResultVal(float resultVal) {
		this.resultVal = resultVal;
	}

}
